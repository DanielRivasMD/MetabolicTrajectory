####################################################################################################

module DPFlow

####################################################################################################

using Avicenna.Flow: Stage, Config
using ..DPCore

####################################################################################################

export flow

####################################################################################################

function dp_stage(config::Dict, prev::Dict)
  config_path = config["config_path"]
  params = DPCore.load_dp_params(config_path)

  if haskey(config, "out_dir")
    params = DPCore.DPParams(params.metadata, params.batches, config["out_dir"])
  end
  return DPCore.run_dp(params)
end

####################################################################################################

const flow = Config("data_processing", [Stage("01_file_loading", dp_stage, "1.0")], "1.0")

####################################################################################################

end

